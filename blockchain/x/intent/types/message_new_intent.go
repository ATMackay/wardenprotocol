package types

import (
	errorsmod "cosmossdk.io/errors"
	codectypes "github.com/cosmos/cosmos-sdk/codec/types"
	sdk "github.com/cosmos/cosmos-sdk/types"
	sdkerrors "github.com/cosmos/cosmos-sdk/types/errors"
)

const TypeMsgNewIntent = "new_intent"

var _ sdk.Msg = &MsgNewIntent{}

func NewMsgNewIntent(creator string, name string, intent *codectypes.Any) *MsgNewIntent {
	return &MsgNewIntent{
		Creator: creator,
		Name:    name,
		Intent:  intent,
	}
}

func (msg *MsgNewIntent) Route() string {
	return RouterKey
}

func (msg *MsgNewIntent) Type() string {
	return TypeMsgNewIntent
}

func (msg *MsgNewIntent) GetSigners() []sdk.AccAddress {
	creator, err := sdk.AccAddressFromBech32(msg.Creator)
	if err != nil {
		panic(err)
	}
	return []sdk.AccAddress{creator}
}

func (msg *MsgNewIntent) GetSignBytes() []byte {
	bz := ModuleCdc.MustMarshalJSON(msg)
	return sdk.MustSortJSON(bz)
}

func (msg *MsgNewIntent) ValidateBasic() error {
	_, err := sdk.AccAddressFromBech32(msg.Creator)
	if err != nil {
		return errorsmod.Wrapf(sdkerrors.ErrInvalidAddress, "invalid creator address (%s)", err)
	}
	return nil
}
